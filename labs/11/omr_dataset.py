import os
import pickle
import sys
import urllib.request

import tensorflow as tf

class OMRDataset:
    MARKS = None  # Set at the bottom of the script for readability

    _URL = "https://ufal.mff.cuni.cz/~straka/courses/npfl114/1920/datasets/"

    @staticmethod
    def parse(example):
        example = tf.io.parse_single_example(example, {
            "image": tf.io.FixedLenFeature([], tf.string),
            "marks": tf.io.VarLenFeature(tf.int64)})
        example["image"] = tf.image.decode_png(example["image"], channels=1)
        example["image"] = tf.image.convert_image_dtype(example["image"], tf.float32)
        example["marks"] = tf.cast(tf.sparse.to_dense(example["marks"]), tf.int32)
        return example

    def __init__(self):
        for dataset in ["train", "dev", "test"]:
            path = "omr.{}.tfrecord".format(dataset)
            if not os.path.exists(path):
                print("Downloading file {}...".format(path), file=sys.stderr)
                urllib.request.urlretrieve("{}/{}".format(self._URL, path), filename=path)

            setattr(self, dataset, tf.data.TFRecordDataset(path))


